func blockFromRows(rows [4][4]byte) block {
	var out block
	for row := 0; row < 4; row++ {
		for col := 0; col < 4; col++ {
			out[col][row] = rows[row][col]
		}
	}
	return out
}

func roundKeyFromRows(rows [4][4]byte) roundkey {
	var out roundkey
	for row := 0; row < 4; row++ {
		for col := 0; col < 4; col++ {
			out[col][row] = rows[row][col]
		}
	}
	return out
}

