func TestEncryptBlock_AES128StandardVector(t *testing.T) {
	logTestLifecycle(t)

	plaintext := [16]byte{
		0x00, 0x11, 0x22, 0x33,
		0x44, 0x55, 0x66, 0x77,
		0x88, 0x99, 0xaa, 0xbb,
		0xcc, 0xdd, 0xee, 0xff,
	}
	key := [16]byte{
		0x00, 0x01, 0x02, 0x03,
		0x04, 0x05, 0x06, 0x07,
		0x08, 0x09, 0x0a, 0x0b,
		0x0c, 0x0d, 0x0e, 0x0f,
	}
	expectedCiphertext := [16]byte{
		0x69, 0xc4, 0xe0, 0xd8,
		0x6a, 0x7b, 0x04, 0x30,
		0xd8, 0xcd, 0xb7, 0x80,
		0x70, 0xb4, 0xc5, 0x5a,
	}

	gotErr, state := getBlock(plaintext)
	if gotErr != nil {
		t.Fatalf("getBlock returned error for plaintext: %v", gotErr)
	}

	wantErr, want := getBlock(expectedCiphertext)
	if wantErr != nil {
		t.Fatalf("getBlock returned error for ciphertext: %v", wantErr)
	}

	gotErr, roundKeys := generateRoundKeys(key)
	if gotErr != nil {
		t.Fatalf("generateRoundKeys returned error: %v", gotErr)
	}

	preFinalRound := state.xorRoundKey(roundKeys[0])
	for round := 1; round < 10; round++ {
		preFinalRound = preFinalRound.applyFullRound(roundKeys[round])
	}
	preFinalRound = preFinalRound.substituteBytes()
	preFinalRound = preFinalRound.shiftRows()

	got := state.EncryptBlock(key)

	if got == preFinalRound {
		t.Fatalf("EncryptBlock returned the state before the final AddRoundKey step")
	}

	if got != want {
		t.Fatalf("encryptBlock mismatch\n got: %#v\nwant: %#v", got, want)
	}
}

func TestEncryptThenDecryptBlock_AES128StandardVector(t *testing.T) {
	logTestLifecycle(t)

	plaintext := [16]byte{
		0x00, 0x11, 0x22, 0x33,
		0x44, 0x55, 0x66, 0x77,
		0x88, 0x99, 0xaa, 0xbb,
		0xcc, 0xdd, 0xee, 0xff,
	}
	key := [16]byte{
		0x00, 0x01, 0x02, 0x03,
		0x04, 0x05, 0x06, 0x07,
		0x08, 0x09, 0x0a, 0x0b,
		0x0c, 0x0d, 0x0e, 0x0f,
	}
	expectedCiphertext := [16]byte{
		0x69, 0xc4, 0xe0, 0xd8,
		0x6a, 0x7b, 0x04, 0x30,
		0xd8, 0xcd, 0xb7, 0x80,
		0x70, 0xb4, 0xc5, 0x5a,
	}

	gotErr, plainBlock := getBlock(plaintext)
	if gotErr != nil {
		t.Fatalf("getBlock returned error for plaintext: %v", gotErr)
	}

	encrypted := plainBlock.EncryptBlock(key)
	encryptedData := encrypted.getData()
	if encryptedData != expectedCiphertext {
		t.Fatalf("encryptBlock mismatch\n got: %#v\nwant: %#v", encryptedData, expectedCiphertext)
	}

	decrypted := encrypted.DecryptBlock(key)
	decryptedData := decrypted.getData()
	if decryptedData != plaintext {
		t.Fatalf("decryptBlock mismatch\n got: %#v\nwant: %#v", decryptedData, plaintext)
	}
}

