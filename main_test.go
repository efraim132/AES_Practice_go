package main

import (
	"fmt"
	"testing"

	"github.com/charmbracelet/log"
)

func TestGenerateRoundKeys_AES128StandardVector(t *testing.T) {
	key := [16]byte{0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f}

	gotErr, got := generateRoundKeys(key)
	if gotErr != nil {
		t.Fatalf("generateRoundKeys returned error: %v", gotErr)
	}

	expected := []roundkey{
		{word{0x00, 0x01, 0x02, 0x03}, word{0x04, 0x05, 0x06, 0x07}, word{0x08, 0x09, 0x0a, 0x0b}, word{0x0c, 0x0d, 0x0e, 0x0f}},
		{word{0xd6, 0xaa, 0x74, 0xfd}, word{0xd2, 0xaf, 0x72, 0xfa}, word{0xda, 0xa6, 0x78, 0xf1}, word{0xd6, 0xab, 0x76, 0xfe}},
		{word{0xb6, 0x92, 0xcf, 0x0b}, word{0x64, 0x3d, 0xbd, 0xf1}, word{0xbe, 0x9b, 0xc5, 0x00}, word{0x68, 0x30, 0xb3, 0xfe}},
		{word{0xb6, 0xff, 0x74, 0x4e}, word{0xd2, 0xc2, 0xc9, 0xbf}, word{0x6c, 0x59, 0x0c, 0xbf}, word{0x04, 0x69, 0xbf, 0x41}},
		{word{0x47, 0xf7, 0xf7, 0xbc}, word{0x95, 0x35, 0x3e, 0x03}, word{0xf9, 0x6c, 0x32, 0xbc}, word{0xfd, 0x05, 0x8d, 0xfd}},
		{word{0x3c, 0xaa, 0xa3, 0xe8}, word{0xa9, 0x9f, 0x9d, 0xeb}, word{0x50, 0xf3, 0xaf, 0x57}, word{0xad, 0xf6, 0x22, 0xaa}},
		{word{0x5e, 0x39, 0x0f, 0x7d}, word{0xf7, 0xa6, 0x92, 0x96}, word{0xa7, 0x55, 0x3d, 0xc1}, word{0x0a, 0xa3, 0x1f, 0x6b}},
		{word{0x14, 0xf9, 0x70, 0x1a}, word{0xe3, 0x5f, 0xe2, 0x8c}, word{0x44, 0x0a, 0xdf, 0x4d}, word{0x4e, 0xa9, 0xc0, 0x26}},
		{word{0x47, 0x43, 0x87, 0x35}, word{0xa4, 0x1c, 0x65, 0xb9}, word{0xe0, 0x16, 0xba, 0xf4}, word{0xae, 0xbf, 0x7a, 0xd2}},
		{word{0x54, 0x99, 0x32, 0xd1}, word{0xf0, 0x85, 0x57, 0x68}, word{0x10, 0x93, 0xed, 0x9c}, word{0xbe, 0x2c, 0x97, 0x4e}},
		{word{0x13, 0x11, 0x1d, 0x7f}, word{0xe3, 0x94, 0x4a, 0x17}, word{0xf3, 0x07, 0xa7, 0x8b}, word{0x4d, 0x2b, 0x30, 0xc5}},
	}

	if len(got) != len(expected) {
		t.Fatalf("expected %d round keys, got %d", len(expected), len(got))
	}

	for i := range expected {
		log.Info(fmt.Sprintf("Testing RoundKey [%v]", i), "Expected", expected[i], "got", got[i])
		if got[i] != expected[i] {
			t.Fatalf("round key %d mismatch\n got: %#v\nwant: %#v", i, got[i], expected[i])
		}
	}
}
